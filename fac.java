   class fac
{
   public static void main (String[] args)
    {
 int x=5,i,j;
        int fac=1,ans=1;
        for(j=1;j<=1;j++)
        {
            fac=fac*j;
        }
        for( i=1;i<=1;i++)
        {                                                                                            
            ans+=1+x+(x*i/fac);
        }
        System.out.println("Answer : "+ans);
    }
}
 